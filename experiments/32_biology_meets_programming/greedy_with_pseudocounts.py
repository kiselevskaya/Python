def greedy_motif_search_with_pseudocounts(dna, k, t):
    best_motifs = []
    for i in range(0, t):
        best_motifs.append(dna[i][0:k])
    n = len(dna[0])
    for e in range(n-k+1):
        motifs = []
        motifs.append(dna[0][e:e+k])
        for j in range(1, t):
            prof = profile_with_pseudocounts(motifs[0:j])
            motifs.append(profile_most_probable_pattern(dna[j], k, prof))
        if get_score(motifs) < get_score(best_motifs):
            best_motifs = motifs
    return best_motifs


